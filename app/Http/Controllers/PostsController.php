    {
        $post = new Posts;
        $post->postId = $request->postId;
        $post->title = $request->title;
        $post->duration = $request->duration;
        $post->resolution = $request->resolution;
        $post->description = $request->description;
        $post->save();

    return redirect()->route('allposts');
